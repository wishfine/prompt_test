V2_FILE = "../physics_sampled_5000_per_difficulty_v2.jsonl"
OUTPUT_FILE = "physics_sampled_50_v2_test_data.jsonl"

def main():
    if not os.path.exists(V2_FILE):
        print(f"Error: 找不到 V2 源文件 {V2_FILE}")
        return

    extracted_items = []
    print(f"正在读取并提取最新这 50 道测试题对应的 V2 带图原数据...")
    
    with open(V2_FILE, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                try:
                    item = json.loads(line)
                    qid = item.get("question_id")
                    if qid in TESTED_IDS:
                        extracted_items.append(item)
                except Exception:
                    continue

    print(f"提取完成！成功从 V2 中抽取到 {len(extracted_items)} / 50 道对齐题目。")

    # 覆盖导出到原本的提取文件中
    with open(OUTPUT_FILE, "w", encoding="utf-8") as out_f:
        for item in extracted_items:
            out_f.write(json.dumps(item, ensure_ascii=False) + "\n")

    # 同时写入一份本地记忆文件，确保您在终端重跑 --fixed 时也可以直接锁定这 50 题
